package com.javatpoint.beans;  
  
public class Emp {  
private int id;  
private String ime;  
private float cijena;  
private String opis;  
  
public int getId() {  
    return id;  
}  
public void setId(int id) {  
    this.id = id;  
}  
public String getName() {  
    return ime;  
}  
public void setName(String ime) {  
    this.ime = ime;  
}  
public float getSalary() {  
    return cijena;  
}  
public void setSalary(float cijena) {  
    this.cijena = cijena;  
}  
public String getDesignation() {  
    return opis;  
}  
public void setDesignation(String opis) {  
    this.opis = opis;  
}  
  
}  